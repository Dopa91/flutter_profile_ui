class FairytailChar {
  final String name;
  final String occupation;
  final String birthday;
  final int age;
  final String guild;
  final String magic;
  final String loves;
  final String team;
  final int battles;

  FairytailChar({
    required this.battles,
    required this.magic,
    required this.loves,
    required this.team,
    required this.name,
    required this.occupation,
    required this.birthday,
    required this.age,
    required this.guild,
  });
}
